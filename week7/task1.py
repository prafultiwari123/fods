import numpy as np

# Create numpy array
arr = np.array([1, 2, 3, 4, 5])

# Sum of elements
arr_sum = np.sum(arr)

# Average of elements
arr_avg = np.mean(arr)

# Maximum value
arr_max = np.max(arr)

# Minimum value
arr_min = np.min(arr)

print("Array:", arr)
print("Sum:", arr_sum)
print("Average:", arr_avg)
print("Maximum:", arr_max)
print("Minimum:", arr_min)
